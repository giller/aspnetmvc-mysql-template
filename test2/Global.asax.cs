
﻿using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Mvc;
using System.Web.WebPages.Scope;
using System.Reflection;
using System.Web.Routing;

namespace MySqlMVCTemplate
{
	public class MvcApplication : System.Web.HttpApplication
	{
		public static void RegisterRoutes (RouteCollection routes)
		{
			routes.IgnoreRoute ("{resource}.axd/{*pathInfo}");

			routes.MapRoute (
				"Default",
				"{controller}/{action}/{id}",
				new { controller = "Home", action = "Index", id = "" }
			);

		}

		public static void RegisterGlobalFilters (GlobalFilterCollection filters)
		{
			filters.Add (new HandleErrorAttribute ());
		}

		protected void Application_BeginRequest()
		{
			var ob = typeof(AspNetRequestScopeStorageProvider).Assembly.GetType ("System.Web.WebPages.WebPageHttpModule")
				.GetProperty ("AppStartExecuteCompleted", BindingFlags.NonPublic | BindingFlags.Static);
			ob.SetValue (null, true, null);
		}

		protected void Application_Start ()
		{
			AreaRegistration.RegisterAllAreas ();
			RegisterGlobalFilters (GlobalFilters.Filters);
			RegisterRoutes (RouteTable.Routes);
		}
	}
}
